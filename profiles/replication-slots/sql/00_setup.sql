\set ON_ERROR_STOP on

DROP SCHEMA IF EXISTS replication_slots CASCADE;
CREATE SCHEMA replication_slots;

CREATE TABLE replication_slots.events (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id integer NOT NULL,
    payload text NOT NULL,
    created_at timestamptz NOT NULL DEFAULT clock_timestamp()
);

CREATE INDEX events_tenant_created_idx
    ON replication_slots.events (tenant_id, created_at DESC);
