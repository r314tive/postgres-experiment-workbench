\set ON_ERROR_STOP on

DROP SCHEMA IF EXISTS connection_pressure CASCADE;
CREATE SCHEMA connection_pressure;

CREATE TABLE connection_pressure.events (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    client_tag text NOT NULL,
    backend_pid integer NOT NULL,
    payload text NOT NULL,
    created_at timestamptz NOT NULL DEFAULT clock_timestamp()
);

CREATE INDEX events_client_tag_idx
    ON connection_pressure.events (client_tag, created_at DESC);
