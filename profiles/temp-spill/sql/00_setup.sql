\set ON_ERROR_STOP on

DROP SCHEMA IF EXISTS temp_spill CASCADE;
CREATE SCHEMA temp_spill;

CREATE TABLE temp_spill.items (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id integer NOT NULL,
    payload text NOT NULL,
    created_at timestamptz NOT NULL DEFAULT clock_timestamp()
);

INSERT INTO temp_spill.items (group_id, payload)
SELECT
    (gs % 2048)::integer,
    md5(gs::text) || md5((gs * 17)::text) || md5((gs * 31)::text)
FROM generate_series(
    1,
    CASE :'profile_size'
        WHEN 'small' THEN 50000
        WHEN 'medium' THEN 200000
        WHEN 'large' THEN 1000000
        ELSE 50000
    END
) AS gs;

CREATE INDEX items_group_id_idx ON temp_spill.items (group_id);

ANALYZE temp_spill.items;
